#!/usr/bin/env python3
"""Generate internal/protocol/blockremap_gen.go: per-target-version block-state ID
remap tables for the multi-version chunk/block translation.

We send the world in canonical 1.21.5 (770) block-state IDs. Newer versions insert
new blocks earlier in the registry, shifting the state IDs of everything after them
(e.g. 1.21.9 adds 63 blocks → lantern 19529→20638). A client on a newer version
decodes our 770 IDs against its own registry and renders the wrong blocks (the
"glass/lanterns look wrong" bug). This emits, per client protocol version, the
state-ID delta for each block whose range moved, so the translation layer can
rewrite chunk palettes and block-update packets to the client's IDs.

Every block keeps the same number of states across these versions (verified), so a
block's whole [minStateId..maxStateId] range shifts by a single constant delta —
making the table a compact list of (min, max, delta) ranges, binary-searched.

Run outside the sandbox (needs network):  python3 scripts/gen_blockremap.py
"""
import json, urllib.request, os

BASE = "https://raw.githubusercontent.com/PrismarineJS/minecraft-data/master/data/pc/{}/blocks.json"
OUT = os.path.join(os.path.dirname(__file__), "..", "internal", "protocol", "blockremap_gen.go")

CANON = "1.21.5"                  # protocol 770, our canonical IDs
TARGETS = {771: "1.21.6", 772: "1.21.8", 773: "1.21.9", 774: "1.21.11"}


def load(ver):
    with urllib.request.urlopen(BASE.format(ver)) as r:
        return {b["name"]: b for b in json.load(r)}


canon = load(CANON)
tables = {}  # version -> sorted [(min, max, delta)]
for ver, dir in sorted(TARGETS.items()):
    tgt = load(dir)
    ranges = []
    skipped = 0
    for name, cb in canon.items():
        tb = tgt.get(name)
        if tb is None:
            continue
        cmin, cmax = cb["minStateId"], cb["maxStateId"]
        tmin, tmax = tb["minStateId"], tb["maxStateId"]
        if (cmax - cmin) != (tmax - tmin):
            skipped += 1  # state count changed — can't shift by a constant (none today)
            continue
        if tmin != cmin:
            ranges.append((cmin, cmax, tmin - cmin))
    ranges.sort()
    tables[ver] = ranges
    print(f"770->{ver} ({dir}): {len(ranges)} shifted ranges, {skipped} skipped (state-count change)")

L = [
    "// Code generated by scripts/gen_blockremap.py; DO NOT EDIT.",
    "",
    "package protocol",
    "",
    "// blockShift is a canonical (770) block-state ID range that moved to a different",
    "// ID in a newer version: every state in [Min,Max] maps to state+Delta.",
    "type blockShift struct {",
    "\tMin, Max uint32",
    "\tDelta    int32",
    "}",
    "",
    "// blockRemaps[clientProtocol] lists the block-state ranges that shifted from the",
    "// canonical 770 IDs to that client version. Sorted by Min for binary search.",
    "var blockRemaps = map[int32][]blockShift{",
]
for ver in sorted(tables):
    L.append(f"\t{ver}: {{")
    for mn, mx, d in tables[ver]:
        L.append(f"\t\t{{{mn}, {mx}, {d}}},")
    L.append("\t},")
L += [
    "}",
    "",
    "// RemapBlockState rewrites a canonical 770 block-state ID to the client",
    "// protocol version's ID. Versions with no table (e.g. 770 itself) pass through.",
    "func RemapBlockState(version int32, state uint32) uint32 {",
    "\ttable := blockRemaps[version]",
    "\tlo, hi := 0, len(table)-1",
    "\tfor lo <= hi {",
    "\t\tmid := (lo + hi) / 2",
    "\t\tr := table[mid]",
    "\t\tswitch {",
    "\t\tcase state < r.Min:",
    "\t\t\thi = mid - 1",
    "\t\tcase state > r.Max:",
    "\t\t\tlo = mid + 1",
    "\t\tdefault:",
    "\t\t\treturn uint32(int32(state) + r.Delta)",
    "\t\t}",
    "\t}",
    "\treturn state",
    "}",
    "",
    "// HasBlockRemap reports whether a client version needs block-state translation.",
    "func HasBlockRemap(version int32) bool { return len(blockRemaps[version]) > 0 }",
    "",
]
with open(OUT, "w") as f:
    f.write("\n".join(L))
print(f"wrote {OUT}")
